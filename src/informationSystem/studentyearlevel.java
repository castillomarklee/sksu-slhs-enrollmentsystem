    private void tblyearlevelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblyearlevelMouseClicked
        try {
            int yearlevelrow = tblyearlevel.getSelectedRow();
            
            String yearlevelrow1 = (tblyearlevel.getModel().getValueAt(yearlevelrow, 1).toString());
            
            String yearlevelquery = "SELECT \n" +
"  yearlevel.yearlevelid, \n" +
"  yearlevel.yearlevelname, \n" +
"  category.categoryname, \n" +
"  yearlevel.trackstrandname\n" +
"FROM \n" +
"  public.category, \n" +
"  public.yearlevel\n" +
"WHERE \n" +
"  category.categoryid = yearlevel.categoryid and yearlevelname='" + yearlevelrow1 + "'";
            
            pstmt = conn.prepareStatement(yearlevelquery);
            rs = pstmt.executeQuery();
            
            if(rs.next()){
                txtyyearlevelid.setText(rs.getString(1));
                txtyearlevelname.setText(rs.getString(2));
                String categoryname =rs.getString(3);
                cbcategory.setSelectedItem(categoryname);
                String trackstrand = rs.getString(4);
                cbtrackstrand.setSelectedItem(trackstrand);
                yearlevelid = rs.getString(1);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_tblyearlevelMouseClicked

