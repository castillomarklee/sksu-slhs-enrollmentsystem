    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int askupdate = JOptionPane.showConfirmDialog(this, "Do you want to update this data?", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if(askupdate == JOptionPane.YES_OPTION){
            try {
                conn = mycon.con;
                String updatequery = "update yearlevel set yearlevelid=?, yearlevelname=?, categoryid=?, trackstrandname=? where yearlevelid=?";
                stmt = conn.createStatement();
                rs = stmt.executeQuery("select * from category where categoryname like'" + cbcategory.getSelectedItem().toString() + "'");
                
                while(rs.next()){
                    categoryid = rs.getString(1);
                }
                pstmt = conn.prepareStatement(updatequery);
                pstmt.setString(1, txtyyearlevelid.getText().toUpperCase());
                pstmt.setString(2, txtyearlevelname.getText().toUpperCase());
                pstmt.setString(3, categoryid);
                pstmt.setString(4, cbtrackstrand.getSelectedItem().toString());
                pstmt.setString(5, yearlevelid);
                pstmt.execute();


                yearleveltable();
                JOptionPane.showMessageDialog(this, "Data has been updated");
                txtyearlevelname.setText(null);
                txtyyearlevelid.setText(null);
                cbcategory.setSelectedIndex(0);
                cbtrackstrand.setSelectedIndex(0);
                txtsearch.setText(null);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

