    private void tbladminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbladminMouseClicked
        try {
            int adminrow = tbladmin.getSelectedRow();
            
            String adminrow1 = (tbladmin.getModel().getValueAt(adminrow, 0).toString());
            
            conn = mycon.con;
            String query = "select * from admin where employeeid='" + adminrow1 + "'";
            pstmt = conn.prepareStatement(query);
            rs = pstmt.executeQuery();
            
            while(rs.next()){
                txtfirstname.setText(rs.getString(1));
                txtmiddlename.setText(rs.getString(2));
                txtlastname.setText(rs.getString(3));
                txtage.setText(rs.getString(4));
                txtaddress.setText(rs.getString(5));
                txtusername.setText(rs.getString(6));
                txtpassword.setText(rs.getString(7));
                txtemployeeid.setText(rs.getString(8));
                cbgender.setSelectedItem(rs.getString(9));
                employeeid = rs.getString(8);
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_tbladminMouseClicked

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int askdelete = JOptionPane.showConfirmDialog(this, "Do you want to delete this data?", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(askdelete == JOptionPane.YES_OPTION){
            if(txtemployeeid.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Please select the data first");
            }else{
                try {
                    conn = mycon.con;
                    String query = "delete from admin where employeeid=?";
                    pstmt = conn.prepareStatement(query);
                    pstmt.setString(1, txtemployeeid.getText().toUpperCase());
                    pstmt.execute();
                    
                    JOptionPane.showMessageDialog(this, "Data has been deleted");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

