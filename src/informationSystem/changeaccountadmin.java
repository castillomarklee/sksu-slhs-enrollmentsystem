    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ask = JOptionPane.showConfirmDialog(this, "Do you want to update this data?", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(ask == JOptionPane.YES_NO_OPTION){
            try {
                conn = mycon.con;
                String query = "update admin set firstname=?, middlename=?, lastname=?, age=?, address=?, username=?, password=?, employeeid=?, gender=? where employeeid=?";
                pstmt = conn.prepareStatement(query);
                pstmt.setString(1, txtfirstname.getText().toUpperCase());
                pstmt.setString(2, txtmiddlename.getText().toUpperCase());
                pstmt.setString(3, txtlastname.getText().toUpperCase());
                pstmt.setInt(4, Integer.parseInt(txtage.getText()));
                pstmt.setString(5, txtaddress.getText().toUpperCase());
                pstmt.setString(6, txtusername.getText().toUpperCase());
                pstmt.setString(7, txtpassword.getText().toUpperCase());
                pstmt.setString(8, txtemployeeid.getText().toUpperCase());
                pstmt.setString(9, cbgender.getSelectedItem().toString().toUpperCase());
                pstmt.setString(10, lblemployeeid.getText().toUpperCase());
                pstmt.execute();
                
                JOptionPane.showMessageDialog(this, "Data has been updated");
                admintable();
                txtfirstname.setText(null);
                txtmiddlename.setText(null);
                txtlastname.setText(null);
                txtage.setText(null);
                txtaddress.setText(null);
                txtusername.setText(null);
                txtpassword.setText(null);
                txtemployeeid.setText(null);
                cbgender.setSelectedIndex(0);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

