        void enrollstudent(){
            try {
                conn = mycon.con;
                stmt = conn.createStatement();
                rs = stmt.executeQuery("SELECT \n" +
"  enrolladdsubject.enrollstudentid, \n" +
"  student.studfirst, \n" +
"  student.studmiddle, \n" +
"  student.studlast, \n" +
"  yearlevel.yearlevelname, \n" +
"  section.sectionname\n" +
"FROM \n" +
"  public.enrolladdsubject, \n" +
"  public.student, \n" +
"  public.section, \n" +
"  public.yearlevel\n" +
"WHERE \n" +
"  enrolladdsubject.accountnumber = student.accountnumber AND\n" +
"  enrolladdsubject.sectionid = section.sectionid AND\n" +
"  section.yearlevelid = yearlevel.yearlevelid;");
                
                DefaultTableModel model = ((DefaultTableModel)tblenrollstudent.getModel());
                model.getDataVector().removeAllElements();
                
                
                while(rs.next()){
                    model.addRow(new Object[] {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
            }
        }
        
