    private void tblenrollstudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblenrollstudentMouseClicked
        try {
            int studentenrollrow = tblenrollstudent.getSelectedRow();
            
            String query = (tblenrollstudent.getModel().getValueAt(studentenrollrow, 0).toString());
            
            conn = mycon.con;
            String studentenroll = "SELECT \n" +
"  enrolladdsubject.enrollstudentid, \n" +
"  student.studfirst, \n" +
"  student.studmiddle, \n" +
"  student.studlast, \n" +
"  yearlevel.yearlevelname, \n" +
"  section.sectionname\n" +
"FROM \n" +
"  public.enrolladdsubject, \n" +
"  public.student, \n" +
"  public.section, \n" +
"  public.yearlevel\n" +
"WHERE \n" +
"  enrolladdsubject.accountnumber = student.accountnumber AND\n" +
"  enrolladdsubject.sectionid = section.sectionid AND\n" +
"  section.yearlevelid = yearlevel.yearlevelid and enrolladdsubject.enrollstudentid='" + query + "'";
            
            pstmt = conn.prepareStatement(studentenroll);
            rs = pstmt.executeQuery();
            
            if(rs.next()){
                lblenrollid.setText(rs.getString(1));
                lblfirstname.setText(rs.getString(2));
                lblmiddlename.setText(rs.getString(3));
                lbllastname.setText(rs.getString(4));
                lblyear.setText(rs.getString(5));
                lblsection.setText(rs.getString(6));
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_tblenrollstudentMouseClicked

