    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
       
        try {
            int confirm = JOptionPane.showConfirmDialog(this, "Do you want to save this data?", "Message", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(confirm==JOptionPane.YES_OPTION){
                conn=mycon.con;
                stmt = conn.createStatement();
                rs = stmt.executeQuery("select * from clerk where username='" + txtusername.getText().toUpperCase() + "'");
                
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Username already exist");
                }else{
                    String query = "insert into clerk values(?,?,?,?,?,?,?,?)";
                pstmt = conn.prepareStatement(query);
                pstmt.setString(1, txtfirstname.getText().toUpperCase());
                pstmt.setString(2, txtmiddlename.getText().toUpperCase());
                pstmt.setString(3, txtlastname.getText().toUpperCase());
                pstmt.setString(4, txtage.getText());
                pstmt.setString(5, cbgender.getSelectedItem().toString());
                pstmt.setString(7, txtusername.getText().toUpperCase());
                pstmt.setString(8, txtpassword.getText().toUpperCase());
                pstmt.setString(6, txtaddress.getText());
         
                
                pstmt.execute();
                JOptionPane.showMessageDialog(this, "Data has been saved");
                txtfirstname.setText(null);
                txtmiddlename.setText(null);
                txtlastname.setText(null);
                txtage.setText(null);
                txtaddress.setText(null);
                txtusername.setText(null);
                txtpassword.setText(null);
                txtfieldemployerid.setText(null);
                cbgender.setSelectedIndex(0);
                table();
                
                }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Save Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnsaveActionPerformed

