    private void tblaccountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblaccountMouseClicked
        try {
            int clerkrow = tblaccount.getSelectedRow();
            
            String clerkrow1 = (tblaccount.getModel().getValueAt(clerkrow, 0).toString());
            
            conn = mycon.con;
            String query = "select * from clerk where clerkid='" + clerkrow1 + "'";
            pstmt = conn.prepareStatement(query);
            rs = pstmt.executeQuery();
            
            if(rs.next()){
                txtfirstname.setText(rs.getString(1));
                txtmiddlename.setText(rs.getString(3));
                txtlastname.setText(rs.getString(3));
                txtage.setText(rs.getString(4));
                txtaddress.setText(rs.getString(5));
                cbgender.setSelectedItem(rs.getString(6));
                txtusername.setText(rs.getString(7));
                txtpassword.setText(rs.getString(8));
                clerkid = rs.getString(9);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_tblaccountMouseClicked

